public class Customer {
	private String name;
	private boolean startOrder;
	
	
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	public boolean isStartOrder() {
		return startOrder;
	}

	public void setStartOrder(boolean startOrder) {
		this.startOrder = startOrder;
	}
	
}
